package com.gongdaeobba.dao;

public class hdd {
	public int power;
	public String company;
	public String interfaceForm;
	public int quantity;
	public int cycleCnt;
	public String useDv;
	public boolean lowElectironic;
	public boolean nasHdd;
	
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getInterfaceForm() {
		return interfaceForm;
	}
	public void setInterfaceForm(String interfaceForm) {
		this.interfaceForm = interfaceForm;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public int getCycleCnt() {
		return cycleCnt;
	}
	public void setCycleCnt(int cycleCnt) {
		this.cycleCnt = cycleCnt;
	}
	public String getUseDv() {
		return useDv;
	}
	public void setUseDv(String useDv) {
		this.useDv = useDv;
	}
	public boolean isLowElectironic() {
		return lowElectironic;
	}
	public void setLowElectironic(boolean lowElectironic) {
		this.lowElectironic = lowElectironic;
	}
	public boolean isNasHdd() {
		return nasHdd;
	}
	public void setNasHdd(boolean nasHdd) {
		this.nasHdd = nasHdd;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
}
