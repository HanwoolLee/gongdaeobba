package com.gongdaeobba.dao;

public class ssd {
	public int power;
	public String company;
	public String interfaceForm;
	public int quantity;
	public int readSpeed;
	public int writeSpeed;

	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getInterfaceForm() {
		return interfaceForm;
	}
	public void setInterfaceForm(String interfaceForm) {
		this.interfaceForm = interfaceForm;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public int getReadSpeed() {
		return readSpeed;
	}
	public void setReadSpeed(int readSpeed) {
		this.readSpeed = readSpeed;
	}
	public int getWriteSpeed() {
		return writeSpeed;
	}
	public void setWriteSpeed(int writeSpeed) {
		this.writeSpeed = writeSpeed;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
}
